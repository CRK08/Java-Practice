public class Bitwise_Operators {
    public static void main(String[] args) {
        // Demonstrates bitwise operators (&, ^, |)
        int i = 423, j = 526;
        System.out.println(i & j);  // Bitwise AND
        System.out.println(i ^ j);  // Bitwise XOR
        System.out.println(i | j);  // Bitwise OR
    }
}