class Even {
    public static void main(String[] args) {
        // Print even numbers from 2 to 10 using do-while loop
        int i = 2;
        do {
            System.out.println(i); // Print current even number
            i = i + 2; // Increment by 2 to get next even number
        } while (i <= 10); // Condition check after loop execution
    }
}
